op = input("Enter the operator you want to use: ")
a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))
if op == '+':
    if a==56 and b==9:
        print(77)
    else:
        c = a+b
        print(c)
elif op == '-':
    c = a-b
    print(c)
elif op == '*':
    if a==45 and b==3:
        print(555)
    else:
        c = a*b
        print(c)
elif op == '/':
    if a==56 and b==6:
        print(4)
    else:
        c = a/b
        print(c)